public class Slide13b {
  public static void main(String[] args) {
    int c=3;
    c += 7;  System.out.println("c+7 = " + c);
    c -= 5;  System.out.println("c-5 = " + c);
    c *= 6;  System.out.println("c*6 = " + c);
    c /= 3;  System.out.println("c/3 = " + c);
    c %= 3;  System.out.println("c%3 = " + c);
  }
}